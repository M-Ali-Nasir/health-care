                <h3>How is epilepsy diagnosed?</h3>
                <p>Technically, if you experience two or more seizures that weren’t caused by a known medical condition
                    — for example, from alcohol withdrawal or low blood sugar — you’re considered to have epilepsy.
                    Before making a diagnosis, your healthcare provider (or epilepsy specialist) will perform a physical
                    exam, take your medical history and may order blood work (to rule out other causes). They may ask
                    about your symptoms during the seizure and conduct other tests, as well.</p>
                <p>Your healthcare provider will ask you or your family member (who’s witnessed your seizure) if you
                    experienced any of the following during a seizure:</p>
                <ul>
                    <li>Muscle jerks.</li>
                    <li>Muscle stiffness.</li>
                    <li>Loss of bowel or bladder control (you peed or pooped during the seizure).</li>
                    <li>Change in breathing.</li>
                    <li>Had a blank stare.</li>
                    <li>Lost consciousness.</li>
                    <li>Had problems talking or understanding what was said to you.</li>
                </ul>
                <h3>What tests will be done to diagnose this condition?</h3>
                <p>Tests include:</p>
                <ul>
                    <li>Electroencephalography (EEG): This test measures the electrical activity in your brain. Certain
                        abnormal electrical patterns are related to seizures.</li>
                    <li>Brain scans: Magnetic resonance imaging (MRI) to look for such things as tumors, infections or
                        blood vessel abnormalities.</li>
                </ul>
