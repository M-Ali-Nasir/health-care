                <h3>What is Epilepsy?</h3>
                <p>Epilepsy is the fourth most common neurological disorder in the world. If you have epilepsy, surges
                    of electrical activity in your brain can cause recurring seizures. We can help you understand
                    everything you need to know about epilepsy and what you can do about it. </p>
                <p>Epilepsy is a brain disorder that causes recurring, unprovoked seizures. Your doctor may diagnose you
                    with epilepsy if you have two unprovoked seizures or one unprovoked seizure with a high risk of
                    more. Not all seizures are the result of epilepsy. Seizures may relate to a brain injury or a family
                    trait, but often the cause is completely unknown. </p>
                <p>The word "epilepsy" simply means the same thing as "seizure disorders." It does not state anything
                    about the cause of the person's seizures or their severity.</p>

                <h3>What is Seizure?</h3>
                <p> A seizure is a sudden, uncontrolled burst of electrical activity in the brain. This abnormal
                    activity can disrupt the way the brain normally works, leading to various physical and behavioral
                    symptoms. Seizures can last from a few seconds to several minutes and vary greatly depending on the
                    part of the brain affected.</p>
                <h1>How Seizure Happen</h1>
                <p>The brain is made up of billions of nerve cells, called neurons, that communicate with each other
                    through electrical signals. Normally, these signals are carefully coordinated, allowing the brain to
                    function properly. However, during a seizure, there is an abnormal surge of electrical activity in
                    the brain. This surge can cause the brain's normal processes to be temporarily disrupted.</P>
                <h1>How common is epilepsy?</h1>
                <p>Almost 3.4 million Americans suffer from epilepsy. Of this total, 470,000 are children and 3 million
                    are adults. Every year in the United States, there are 150,000 newly reported cases of epilepsy.
                    There are roughly 65 million people with epilepsy in the world.</P>
                <h1>What happens in your brain when you have epilepsy?</h1>
                <p>All parts of your body send and receive messages to and from the brain's cells. A constant electrical
                    impulse that moves from cell to cell transmits these messages. This regular electrical impulse
                    pattern is disturbed by epilepsy. In fact, in one or more parts of your brain, there are electrical
                    energy bursts between cells that like an unpredictable lightning storm. Your awareness (including
                    loss of consciousness), sensations, emotions, and muscle actions are all disturbed by this
                    electrical disruption.</p>
                <h1>How can I figure out my seizure triggers?</h1>
                <p>Some people discover that their seizures occur consistently during certain times of the day or around
                    certain events or other factors. You may want to track your seizures — and the events around your
                    seizures — to see if there’s a pattern.</p>
                <p>Keep a seizure diary in which you record the time of day each seizure occurs, any unusual events or
                    unique circumstances around the seizure, and your feelings at the time.If you suspect you’ve
                    identified a trigger, track that trigger to find out if it’s really a trigger. For example, if you
                    think caffeine is a seizure trigger, do you have a seizure after consuming every caffeinated food or
                    beverage, after “x” number of caffeinated foods/beverages or at certain times of day after consuming
                    caffeine? Caffeine may or may not be the trigger when thoroughly reviewed.</p>
                <h3>Symptoms of a Seizure</h3>
                <p>The symptoms of a seizure depend on the part of the brain where the abnormal activity occurs and how
                    far it spreads. Common symptoms includeThe person may lose consciousness, become confused, or have a
                    blank stare.</P>
                <pre>
<b>Changes in Awareness:</b>
    The person may lose consciousness, become confused, or have a blank stare.

<b>Uncontrolled Movements:</b> 
    This can include jerking, twitching, or stiffning of muscles. Movements might be limited to one part of the body or affect the entire body.

<b>Sensory Changes:</b> 
    The person might experience unusual sensations, such as tingling, dizziness, or seeing flashing lights.

<b>Emotional or Psychological Symptoms:</b>
     Some seizures can cause sudden feelings of fear, anxiety, or déjà vu (a feeling that something has happened before).

<b>Automatisms:</b>
    Repetitive movements, such as lip-smacking, hand-rubbing, or walking in circles, may occur.

                </pre>
