                <h3>Causes Of Epilep sy</h3>
                <p>Most of the time (in up to 70% of cases), the cause of seizures is not known. Known causes include:
                </p>
                <ul>
                    <li><b>Genetics.</b> Some types of epilepsy (like juvenile myoclonic epilepsy and childhood absence
                        epilepsy) are more likely to run in families (inherited). Researchers believe that although
                        there’s some evidence that specific genes are involved, the genes only increase the risk of
                        epilepsy, and other factors may be involved. There are certain epilepsies that result from
                        abnormalities that affect how brain cells can communicate with each other and can lead to
                        abnormal brain signals and seizures.</li>
                    <li><b>Mesial temporal sclerosis.</b> This is a scar that forms in the inner part of your temporal
                        lobe (part of your brain near your ear) that can give rise to focal seizures.</li>
                    <li><b>Head injuries.</b> Head injuries can result from vehicular accidents, falls or any blow to
                        the head.</li>
                    <li><b>Brain infections. </b>Infections can include brain abscess, meningitis, encephalitis and
                        neurocysticercosis.</li>
                    <li><b>Immune disorders.</b> Conditions that cause your immune system to attack brain cells (also
                        called autoimmune diseases) can lead to epilepsy.</li>
                    <li><b>Developmental disorders.</b> Birth abnormalities affecting the brain are a frequent cause of
                        epilepsy, particularly in people whose seizures aren’t controlled with anti-seizure medications.
                        Some birth abnormalities known to cause epilepsy include focal cortical dysplasia,
                        polymicrogyria and tuberous sclerosis. There’s a wide range of other brain malformations known
                        to cause epilepsy.</li>
                    <li><b>Metabolic disorders.</b> People with a metabolic condition (how your body obtains energy for
                        normal functions) can have epilepsy. Your healthcare provider can detect many of these disorders
                        through genetic tests.</li>
                    <li><b>Brain conditions and brain vessel abnormalities.</b> Brain health issues that can cause
                        epilepsy include brain tumors, strokes, dementia and abnormal blood vessels, such as
                        arteriovenous malformations.</li>
                </ul>


                <h3>Types Of Seizures</h3>
                <p>Seizures are categorized into two main types: generalized and focal. Each type has several subtypes
                    with distinct characteristics. As follows</p>

                <h1>1. Generalized Seizures </h1>
                <P>These seizures involve both sides of the brain and usually cause a loss of consciousness.</P>

                <b>Tonic-Clonic Seizures (Grand Mal):</b>
                <ul>
                    <li><b>Tonic Phase:</b> Sudden muscle stiffening, which may cause the person to fall.</li>
                    <li><b>Clonic Phase:</b> Rhythmic jerking or twitching of the arms and legs.</li>
                    <li><b>Postictal Phase:</b> After the seizure, the person may feel confused, tired, or have a
                        headache.</li>
                </ul>
                <b>Absence Seizures (Petit Mal):</b>
                <ul>
                    <li>Brief lapses in awareness, often lasting only a few seconds.</li>
                    <li>The person may stare blankly and may not respond to others.</li>
                    <li>Often mistaken for daydreaming, especially in children.</li>
                </ul>
                <b>Atonic Seizures (Drop Attacks):</b>
                <ul>
                    <li>Sudden loss of muscle tone, leading to collapse or falling.</li>
                    <li>The person may need to wear protective headgear due to frequent falls.</li>
                    <li>These seizures are very brief, often lasting less than 15 seconds.</li>
                </ul>
                <b>Myoclonic Seizures:</b>
                <ul>
                    <li>Sudden, brief, shock-like jerks or twitches, usually affecting the upper body.</li>
                    <li>The person may remain conscious during the seizure.</li>
                    <li>Often occur shortly after waking up.</li>
                </ul>
                <b>Tonic Seizures:</b>
                <ul>
                    <li>Sudden stiffening of muscles, typically in the arms, legs, or back.</li>
                    <li>The person may fall if standing when the seizure occurs.</li>
                    <li>Usually short, lasting less than 20 seconds.</li>
                </ul>
                <b>Clonic Seizures:</b>
                <ul>
                    <li>jerking movements of the muscles, usually on both sides of the body.</li>
                    <li>The person may lose consciousness.</li>
                    <li>Less common and usually seen in infants.</li>
                </ul>
                <h1> 2.Focal Seizures (Partial Seizures)</h1>
                <p>These seizures start in one specific area of the brain and can be either aware or impaired awareness.
                </p>

                <b>Simple Focal Seizures (Focal Aware Seizures):</b>
                <ul>
                    <li>The person remains aware and conscious during the seizure.</li>
                    <li>May experience unusual sensations, such as tingling, strange smells or tastes, or visual
                        distortions.</li>
                    <li>Can cause involuntary jerking of a body part, such as an arm or leg.</li>
                    <li>These seizures typically last for a short period, usually under 2 minutes.</li>
                </ul>
                <b>Complex Focal Seizures (Focal Impaired Awareness Seizures):</b>
                <ul>
                    <li>The person’s awareness is altered or lost during the seizure.</li>
                    <li>They may seem confused and may perform repetitive movements (automatisms) like hand-rubbing or
                        walking in circles.</li>
                    <li>The person may not remember the seizure or what happened during it.</li>
                    <li>These seizures typically last 1 to 2 minutes.</li>
                </ul>
                <b>Focal Seizures Evolving to Generalized Seizures:</b>
                <ul>
                    <li>The seizure starts in one part of the brain (focal) and then spreads to both sides, becoming a
                        generalized seizure.</li>
                    <li>The person may first experience focal seizure symptoms, followed by a tonic-clonic seizure.</li>
                    <li>This type is sometimes called a "secondary generalized seizure."</li>
                </ul>
